<script>
  import YShelf from '/components/shelf'
  import YButton from '/components/YButton'
  import YHeader from '/common/header'
  import YFooter from '/common/footer'
  import { getStore } from '/utils/storage'
  import axios from 'axios'
  import data from '../../mock/index'
  export default {
    data () {
      return {
        shopList: [],
        addList: [],
        addressId: '0',
        popupOpen: false,
        popupTitle: '管理收货地址',
        num: '', // 立刻购买
        productId: '',
        msg: {
          addressId: '',
          userName: '',
          tel: '',
          streetName: '',
          isDefault: false
        },
        userName: '',
        tel: '',
        streetName: '',
        userId: '',
        orderTotal: 0,
        submit: false,
        submitOrder: '提交订单'
      }
    },
    computed: {
      // 选中的总价格
      checkPrice () {
        let totalPrice = 0
        this.shopList.map((item) => {
          totalPrice += item.productNum * item.salePrice
        })
        return totalPrice.toFixed(2)
      }
    },
    methods: {
      notify (m) {
        this.$notify.error({
          message: m
        })
      },
      subOrder () {
        let msg = '送给你了,不用提交了'
        this.notify(msg)
      },
      // 获取商品信息,将数量也传进去
      _productDet (productId) {
        data.getData()
        axios.post('/goods/productDet', {productId})
          .then((response) => {
            let { data: {list} } = response
            list[0].productNum = this.num
            this.shopList = list
          })
      }
    },
    created () {
      this.userId = getStore('userId')
      let query = this.$route.query
      if (query.productId && query.num) {
        this.productId = query.productId
        this.num = query.num
        this._productDet(this.productId)
      }
    },
    components: {
      YShelf,
      YButton,
      YHeader,
      YFooter
    }
  }
</script>
